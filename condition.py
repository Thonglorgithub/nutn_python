n1 = int(input("please input number : "))
n2 = int(input("please input number : "))
op = input("please input operator + - * /\n")

if op == "+":
    print(n1 + n2)
elif op == "-":
    print(n1 - n2)
elif op == "*":
    print(n1 * n2)
elif op == "/":
    print(n1 / n2)
else:
    print("\n")




